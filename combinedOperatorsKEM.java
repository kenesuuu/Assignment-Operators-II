public class combinedOperatorsKEM {
    public static void main(String[] args) {
        // Define integer variables
        int a = 5;
        int b = 10;

        // Check if both a and b are positive numbers
        if (a > 0 && b > 0) {
            // Add a and b, store the result in a
            a += b;
            // Multiply a by 2, store the result in b
            b *= 2;
        }

        // Print the final values of a and b
        System.out.println("Final value of a: " + a);
        System.out.println("Final value of b: " + b);
    }
}
